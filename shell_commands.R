pwd (print working directory)
ls (list files)
  ls -a (also list hidden)
cd (change directory)
  goto subdirectory cd subdirectory
  goto parent cd ..
  goto home ~cd or cd
  goto absolute path cd /home/user_name/absolute_path (drag n drop a file for path) 
      (use tab to autocomplete)
use up/down for previous commands

Git:
git status (branch info)
git remote -v (list ball remotes)
git remote add ORIGIN URL (adds remote URL with name ORIGIN)
git remote set-url ORIGIN URL (changes the ORIGIN remote URL)
